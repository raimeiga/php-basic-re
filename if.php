問題5:「TRUEを返す」とはどういうことか（戻り値について）を説明せよ
正解5:
返り値とは
・条件式で比較演算子を使って2つの値を比較した場合に発生する値【TRUE（真）or FALSE（偽）】
・算術演算子を使った計算結果（数字）

条件式で比較演算子を使って2つの値を比較すると、論理型データ、つまりTRUE（真）or FALSE（偽）のいずれかの値が返される。このときに返される値を「返り値」という（返されるというより、発生する、と考えたほうがいいかも）。算術演算子を使った計算結果も戻り値として「返された」と考える。

