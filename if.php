問題11:if,elseif、elseの構文を書け

正解11:
------------------------------------------------------------
if (条件式A) {
  条件Aが成り立つときの処理
} elseif (条件式B) {
  条件Bが成り立つときの処理  
} else {
  どの条件も成り立たないときの処理
}
------------------------------------------------------------
※
・ifとelseは1つの条件分岐の中で一度しか記述できないが、
  elseifはいくつでも追加できる
・JavaScriptでは半角スペースを空けてelse ifと記述するが、
  PHPでは半角スペースを空けずにelseifと書くのが一般的。

