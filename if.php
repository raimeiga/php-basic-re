問題3:もし変数$numが10より大きいならば、
「変数$numは10より大きいです」という文字列を出力せよ。

正解3:
------------------------------------------------------------
<?php
$num = 50;

// もし変数$numが10より大きいならば、「変数$numは10より大きいです」という文字列を出力する
if ($num > 10) {
    echo '変数$numは10より大きいです';
}
?>
------------------------------------------------------------
※num=「番号」や「数字」を意味するnumberの略で、慣習的に使われる変数名


