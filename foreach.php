         foreach ($user_names as $index => $value) {
             echo "{$index}:{$value}<br>";
         }
//  配列$user_namesのインデックスを、1つずつ順番に出力する
        foreach ($user_names as $index => $value) {
             echo "{$index}<br>";
        }

//  配列$user_namesの値を、1つずつ順番に出力する
        foreach ($user_names as $index => $value) {
             echo "{$value}<br>";
        }
?>
------------------------------------------------------------

