問：foreach文、break文を使い、配列user_namesに'侍二郎'という文字列が含まれているかどうかを1つずつチェックし、
　　'侍二郎'という文字列が見つかった時点で繰り返し処理を終了するコードをかけ。


正解：
------------------------------------------------------------
<p>
  　<?php
      $user_names = ['侍太郎', '侍一郎', '侍二郎', '侍三郎', '侍四郎'];
 
      // 検索したいユーザー名を代入する変数
    　$target = '侍二郎';
 
      foreach ($user_names as $user_name) {
         echo $user_name . '<br>';
 
        // 変数$user_nameと変数$targetの値が一致すれば、break文で繰り返し処理を強制終了する
         if ($user_name === $target) {
             echo "{$target}さんが見つかったので、繰り返し処理を強制終了します。";
             break;
          }
       }
     ?>
</p>
