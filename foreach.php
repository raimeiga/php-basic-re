問題1:foreachについて、下の問に答えよ。
使用するケースとその例を説明せよ。
構文を書け。今んとこは例文でよい。

正解：
使用するケース：配列や連想配列に対して繰り返し処理を行いたい場合	
例：配列の中に入っている都道府県の名前を順番に取り出し、フォームのセレクトボックスに表示する
構文：
foreach ($user_names as $user_name) {
   echo $user_name;
}
↓
問：配列名$user_namesと変数名$user_nameの説明をせよ

正解
$user_names:要素を取り出す配列名（複数形）
$user_name:取り出した要素を代入する変数名（要素を取り出す配列名の単数形）

問：繰り返し処理の回数は、何を考えればわかるか

正解：「繰り返し処理の回数＝配列や連想配列の要素数」
foreach文は配列や連想配列の各要素を順番に取り出すので、
繰り返し処理の回数が配列や連想配列の要素数と考える。


問:foreach文を使い、配列$user_namesの値（要素）を1つずつ順番に出力せよ。
$user_names = ['侍太郎', '侍一郎', '侍二郎', '侍三郎', '侍四郎'];

正解：
------------------------------------------------------------
<?php
$user_names = ['侍太郎', '侍一郎', '侍二郎', '侍三郎', '侍四郎'];

//  配列$user_namesの値（要素）を1つずつ順番に出力する
foreach ($user_names as $user_name) {
   echo $user_name.'<br>';
}
?>
------------------------------------------------------------

問:foreach文を使い、下記のコードを書け。
配列$user_namesのインデックスと値を、1つずつ順番に出力せよ。
配列$user_namesのインデックスを、1つずつ順番に出力せよ。
配列$user_namesの値を、1つずつ順番に出力せよ。

$user_names = ['侍太郎', '侍一郎', '侍二郎', '侍三郎', '侍四郎'];


